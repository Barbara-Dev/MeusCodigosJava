public class loopifor {
    public static void main(String[] args) {
        //for loop = executa um bloco de c√≥digo uma quantidade limitada de vezes
        for(int i=0; i<=10; i++){
            System.out.println(i);
        }

        for(int a=10; a>=0; a--){
            System.out.println(a);
        }

        System.out.println("Feliz ano novo!");
    }
}
