public class expressoes {
    public static void main(String[] args) {
        // expressões = operandos e operadores
        // operandos = valores, variáveis, números, quantidades
        // operador = + (incremento) - (subtração) * (multiplicação) / (divisão)     % (resto da divisão) (símbolos aritméticos)
        // ++ (incrementa 1) -- (subtrai 1)
        

        int amigos = 10;

        amigos = amigos + 1;
        System.out.println("Amigos: "+amigos);

        int maca = 20;
        maca++;
        System.out.println("Maçãs: "+maca);

        double paes = 10;

        paes = (double) paes / 3;
        System.out.println("Pães: "+paes);

   }
}
